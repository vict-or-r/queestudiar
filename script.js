    document.getElementById("buscador-global").addEventListener("keydown", function (event) {
  if (event.key === "Enter") {
    const texto = this.value.toLowerCase().trim();
    const contenedor = document.getElementById("resultados-busqueda");

    // Limpiar y ocultar si no hay búsqueda
    if (!texto) {
      contenedor.innerHTML = "";
      contenedor.classList.add("d-none");
      return;
    }

    // Búsqueda tolerante por varias palabras
    const palabras = texto.split(" ");
    const resultados = cursosData.filter(curso =>
      palabras.every(p =>
        (curso.curso && curso.curso.toLowerCase().includes(p)) ||
        (curso.descripcion_sencilla && curso.descripcion_sencilla.toLowerCase().includes(p)) ||
        (curso.descripcion_detallada && curso.descripcion_detallada.toLowerCase().includes(p)) ||
        (curso.frase_clave && curso.frase_clave.toLowerCase().includes(p))
      )
    );

    // Mostrar resultados
    mostrarResultadosBusqueda(resultados, texto);
  }
});

function mostrarResultadosBusqueda(cursos, texto) {
  const contenedor = document.getElementById("resultados-busqueda");
  contenedor.innerHTML = "";
  contenedor.classList.remove("d-none");

  contenedor.innerHTML = `<h4 class="mb-4">📘 Resultados para: "<strong>${texto}</strong>"</h4>`;

  if (cursos.length === 0) {
    contenedor.innerHTML += `
      <div class="alert alert-warning">
        ❌ No se encontraron resultados para "<strong>${texto}</strong>".<br>
        Prueba con otro término como nombre de curso, palabra clave o especialidad.
      </div>`;
    return;
  }

  const row = document.createElement("div");
  row.className = "row";

  cursos.forEach(curso => {
    const descripcion = curso.descripcion_sencilla || "📌 Información no disponible";
    const detalle = curso.descripcion_detallada || "";
    const frase = curso.frase_clave ? `<blockquote class="blockquote-footer">💡 ${curso.frase_clave}</blockquote>` : "";
    const salidas = curso.salidas_laborales ? `🧭 ${curso.salidas_laborales}` : "";
    const opiniones = curso.opiniones_expertas ? `🧠 ${curso.opiniones_expertas}` : "";
    const titulacion = curso.titulación_complementaria ? `📜 ${curso.titulación_complementaria}` : "";

    const col = document.createElement("div");
    col.className = "col-md-6 mb-4";

    col.innerHTML = `
      <div class="card h-100 shadow-sm">
        <div class="card-body text-start">
          <h5 class="card-title">🎓 ${curso.curso}</h5>
          <p class="card-text">📝 ${descripcion}</p>
          ${frase}
          ${detalle ? `<p class="text-muted mt-2">${detalle}</p>` : ""}
          ${salidas ? `<p class="text-muted">${salidas}</p>` : ""}
          ${opiniones ? `<p class="text-muted">${opiniones}</p>` : ""}
          ${titulacion ? `<p class="text-muted">${titulacion}</p>` : ""}
        </div>
      </div>`;
    
    row.appendChild(col);
  });

  contenedor.appendChild(row);
}
