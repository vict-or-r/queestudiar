const etapasUnicas = [...new Set(cursosData.map(c => c.etapa))];
const familiasPorEtapa = etapa =>
  [...new Set(cursosData.filter(c => c.etapa === etapa).map(c => c.familia))];
const cursosPorFamilia = (etapa, familia) =>
  cursosData.filter(c => c.etapa === etapa && c.familia === familia);

let etapaSeleccionada = null;
let familiaSeleccionada = null;

function iniciarExploradorCursos() {
  const contenedor = document.getElementById("etapas");
  contenedor.innerHTML = "";
  etapasUnicas.forEach(etapa => {
    const btn = document.createElement("button");
    btn.className = "btn btn-outline-primary";
    btn.textContent = etapa;
    btn.onclick = () => seleccionarEtapa(etapa);
    contenedor.appendChild(btn);
  });
}

function seleccionarEtapa(etapa) {
  etapaSeleccionada = etapa;
  document.getElementById("pantalla-etapas").classList.add("hidden");
  document.getElementById("pantalla-familias").classList.remove("hidden");
  const familias = familiasPorEtapa(etapa);
  const contenedor = document.getElementById("familias");
  contenedor.innerHTML = "";
  familias.forEach(familia => {
    const btn = document.createElement("button");
    btn.className = "btn btn-outline-info";
    btn.textContent = familia;
    btn.onclick = () => seleccionarFamilia(familia);
    contenedor.appendChild(btn);
  });
}

function seleccionarFamilia(familia) {
  familiaSeleccionada = familia;
  document.getElementById("pantalla-familias").classList.add("hidden");
  document.getElementById("pantalla-cursos").classList.remove("hidden");

  const cursos = cursosPorFamilia(etapaSeleccionada, familia);
  const contenedor = document.getElementById("cursos");
  contenedor.innerHTML = "";

  cursos.forEach(curso => {
    const descSencilla = curso.descripcion_sencilla || "📌 Información no disponible";
    const descDetallada = curso.descripcion_detallada || "";
    const frase = curso.frase_clave ? `💡 <em>${curso.frase_clave}</em>` : "";
    const salidas = curso.salidas_laborales ? `🧭 ${curso.salidas_laborales}` : "";
    const opiniones = curso.opiniones_expertas ? `🧠 ${curso.opiniones_expertas}` : "";
    const titulacion = curso.titulación_complementaria ? `📜 ${curso.titulación_complementaria}` : "";

    const col = document.createElement("div");
    col.className = "col-md-6 mb-4";

    col.innerHTML = `
      <div class="card h-100 shadow-sm">
        <div class="card-body text-start">
          <h5 class="card-title">🎓 ${curso.curso}</h5>
          <p class="card-text">📝 ${descSencilla}</p>
          ${frase ? `<blockquote class="blockquote-footer">${frase}</blockquote>` : ""}
          ${descDetallada ? `<p class="text-muted mt-2 small">📚 ${descDetallada}</p>` : ""}
          ${salidas ? `<p class="text-muted small">${salidas}</p>` : ""}
          ${opiniones ? `<p class="text-muted small">${opiniones}</p>` : ""}
          ${titulacion ? `<p class="text-muted small">${titulacion}</p>` : ""}
        </div>
      </div>`;
    contenedor.appendChild(col);
  });
}

